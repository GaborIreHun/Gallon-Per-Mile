// This program calculates the fuel consuption of a vehicle.

class GasMilage
{
    public int milesDriven;

    public int gallons;

    public void SetMilesDriven(int gasMilageMilesDriven)
    {
        milesDriven = gasMilageMilesDriven;
    }

    public int GetMilesDriven()
    {
        return milesDriven;
    }

    public void SetGallons(int gasMilageGallons)
    {
        gallons = gasMilageGallons;
    }

    public int GetGallons()
    {
        return gallons;
    }
        
}
